namespace Webkul\AmazonMagentoConnect\Model\Config\Source;

class ImportType implements \Magento\Framework\Option\ArrayInterface
{
    /**
     * Return options array.
     *
     * @param int $store
     *
     * @return array
     */
    public function toOptionArray($store = null)
    {
        $importProductType = [];

        $importProductType[] = ['value' => 0,'label' => 'Without Variation'];
        $importProductType[] = ['value' => 1,'label' => 'With Variation'];

        return $importProductType;
    }

    /**
     * Get options in "key-value" format.
     *
     * @return array
     */
    public function toArray()
    {
        $optionList = $this->toOptionArray();
        $optionArray = [];
        foreach ($optionList as $option) {
            $optionArray[$option['value']] = $option['label'];
        }

        return $optionArray;
    }
}
